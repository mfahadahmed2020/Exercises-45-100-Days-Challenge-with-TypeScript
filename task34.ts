// Store the names of three favorite pizzas in an array.
const pizzas: string[] = ["Pepperoni", "Margherita", "Supreme"];

// Loop through the array and print a sentence using each pizza's name.
for (const pizza of pizzas) {
    console.log(`I like ${pizza} pizza.`);
}

// Print an additional line outside the loop indicating how much you like pizza.
console.log("I really love pizza!");