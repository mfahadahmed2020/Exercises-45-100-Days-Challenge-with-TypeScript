// Starting with our car object
let car = {
  make: "Toyota",
  model: "Corolla",
  color: "black", // Adding a new property 'color'
  year: 1982,     // Adding the 'year' property
};

// Updating the color and year properties
car.color = "black"; // Updating the 'color' property
car.year = 2007;     // Updating the 'year' property

// Showing the updated car object
console.log(car); // Outputs the car object with the new color and updated year 2007.